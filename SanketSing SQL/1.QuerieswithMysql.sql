SELECT * FROM POSTS WHERE USER_ID = 1 AND CONTENT = 'HELLOW WORLD AGAIN';

-- OPERATOR IN MYSQL : = , < , > , <= , >= , AND , OR , NOT, IN , BETWEEN,
-- LIKE, IS NULL , IS NOT NULL


SELECT * FROM POSTS WHERE CONTENT LIKE '%HELLO%'; -- START WITH ANYTHING END WITH ANYTHING
SELECT * FROM POSTS WHERE CONTENT LIKE '%AGAIN';

-- %AGAIN% SUBSTRING MATCH
-- %AGAIN   STARTS WITH ANYTHING BUT ENDS WITH AGAIN
-- AGAIN%   STARTS WITH AGAIN BUT CAN HAVE ANYTHING AFTER THAT


--SORT THE DATA
SELECT * FROM POSTS ORDER BY CREATED_AT; -- ASC WILL SHOW THE TABLE IN INCREASING ORDER OF THE CREATED_AT COLUMN

SELECT * FROM POSTS ORDER BY CREATED_AT DESC;

SELECT * FROM POSTS WHERE CONTENT LIKE '%WORLD' ORDER BY CREATED_AT ASC;  --STARTS WITH WORLD AND ASENDING VIA CREATED_AT COLUMN

