-- CREATE A POSTS TABLE WITH ID, CONTENT, USER_ID, CREATED_AT COLUMNS

CREATE TABLE POSTS(
    ID INT PRIMARY KEY AUTO_INCREMENT,
    CONTENT VARCHAR(255),
    USER_ID INT,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP

);
-- HERE IF WE DONT GIVE CREATED_AT VALUE THEN IT WILL GET THE VALUE OF CURRENT TIME BY DEFAULT FROM SQL SERVER 

